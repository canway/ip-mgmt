from rest_framework.viewsets import ModelViewSet

from home_application.custom_attributes.filter import CustomAttrFilter
from home_application.custom_attributes.serializers import CustomAttrSerializer
from home_application.mixins import ApiGenericMixin
from home_application.models import CustomAttr


class CustomAttrViewSet(ApiGenericMixin, ModelViewSet):
    # 序列化
    queryset = CustomAttr.objects.all()
    serializer_class = CustomAttrSerializer
    filter_class = CustomAttrFilter

    def perform_create(self, serializer):
        """创建时补充基础Model中的字段"""
        user = serializer.context.get("request").user
        username = getattr(user, "username", "guest")
        serializer.save(create_by=username)
